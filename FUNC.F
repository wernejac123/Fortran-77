C PROGRAM DEMONSTRATING FUNCTIONS
C FIX ADD IMPLICIT NONE TO FUNCTION
C FIX INTRINSIC ERROR WHEN COMPILING WITH GFORTRAN
      INTEGER FUNCTION SM(A, B)
      IMPLICIT NONE
      INTEGER A, B
      SM = A * B

      RETURN
      END
            
      PROGRAM CALLEX

      IMPLICIT NONE
      INTEGER X, Y, SM

      WRITE(*,*) 'ENTER X, Y:'
      READ(*,*) X, Y

      WRITE(*,*) SM(X, Y)

      STOP
      END
